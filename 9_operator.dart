void main(List<String> args) {
  // operator aritmatika
  var a = 5;
  var b = 2;

  print(a + b); //penjumlahan
  print(a - b); //pengurangahan
  print(a / b); //pembagian
  print(a * b); //perkalian
  print(a % b); //modulus sisa pembagian
  print(a ~/ b); // pembagian yang dibulatkan

  print("=============================");
  // icrement dan decrement
  var c = 10;
  var d = 5;
  c++; // c = c + 1
  d--; // d = d - 1

  print(c); //output 11
  print(d); // output 4

  var f = 0;
  f += 5; // f = f + 5 atau f = 0 + 5
  print(f);
}
